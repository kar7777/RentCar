    //Method to register
    private void registerNewCar() {
        try {
            CarBO cbo = new CarBO();
            Car c = new Car();
            c.setId_car(txtIDCar.getText());
            c.setBrand_car((String)cbxBrand.getSelectedItem());
            c.setModel_car((String)cbxModel.getSelectedItem());
            c.setStyle_car((String)cbxStyle.getSelectedItem());
            c.setTransmission_car((String)cbxTransmission.getSelectedItem());
            c.setYear_car((String)cbxYear.getSelectedItem());
            c.setAvailable_car((String)cbxAvailable.getSelectedItem());
            c.setPhoto_car(photo);
            c.setPrice_car(Integer.parseInt(txtPrice.getText()));

                if (cbo.register(c)) {
                    JOptionPane.showMessageDialog(null, "Registered");
                }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }

    
