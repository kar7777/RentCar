    //Method to register
    private void registerNewModel() {
        try {
            ModelBO bbo = new ModelBO();
            Model b = new Model();
            b.setModel_id(txtIDmodel.getText());
            b.setModel_name(txtNameModel.getText());

            if (bbo.register(b)) {
                JOptionPane.showMessageDialog(null, "Model registered");
            }
            clear();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }

    private void clear() {
        txtIDmodel.setText("");
        txtNameModel.setText("");
    }

    //Method to close session
    private void LogOut() {
        JOptionPane.showMessageDialog(null, "Finalized session");
        FrmLogin out = new FrmLogin();
        out.setVisible(true);
        dispose();
    }

    //Method to go admin menu
    private void goMenu() {
        FrmMenuAdmin menu = new FrmMenuAdmin();
        menu.setLocationRelativeTo(null);
        menu.setVisible(true);
        dispose();
    }

