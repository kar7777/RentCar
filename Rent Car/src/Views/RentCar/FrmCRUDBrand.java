    //Method to register
    private void registerNewBrand() {
        try {
            BrandBO bbo = new BrandBO();
            Brand b = new Brand();
            b.setBrand_id(txtID.getText());
            b.setBrand_name(txtName.getText());

                if (bbo.register(b)) {
                    JOptionPane.showMessageDialog(null, "Brand registered");
                }
                clear();
   
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }
    
    private void clear(){
        txtID.setText("");
        txtName.setText("");
    }
    
    
    
    
